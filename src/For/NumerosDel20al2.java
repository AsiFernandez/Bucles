package For;

public class NumerosDel20al2 {

	public static void main(String[] args) {
	
		

		
		
		/*
		 * Ejercicio resuelto con un for
		 * todos los numeros
		 */
		
		for(int i = 20; i >= 2; i--) {
			System.out.println(i);
		}
		
		
		
		/*
		 * Ejercicio resuelto con un for
		 * numeros pares
		 */
		
		for(int i = 20; i >= 2; i=-2) {
			System.out.println(i);
		}
		
		
		
		
		
	}

}
